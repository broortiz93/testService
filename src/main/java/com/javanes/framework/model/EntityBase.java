package com.javanes.framework.model;



public class EntityBase {
	
	private String codReturn;
	private String codValor;
	private String fecha;
	
	public String getCodReturn() {
		return codReturn;
	}
	public void setCodReturn(String codReturn) {
		this.codReturn = codReturn;
	}
	public String getCodValor() {
		return codValor;
	}
	public void setCodValor(String codValor) {
		this.codValor = codValor;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	

}
